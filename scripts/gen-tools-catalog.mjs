// Generates TOOLS.md from the typed manifest registry, so the catalog can
// never drift from what's actually registered. Run: npm run gen:tools
// (tsx executes the TS registry directly; no build step.)
import { writeFileSync } from "node:fs";
import { TOOL_MANIFESTS } from "../src/lib/tools/registry.ts";

const rows = TOOL_MANIFESTS.map((t) => {
  const fns = t.binding.kind === "webhook"
    ? t.binding.functions.map((f) => `\`${f.name}\``).join(", ")
    : `IAI toolset \`${t.binding.token}\``;
  const upstream = t.upstream
    ? `[${t.upstream.project}](${t.upstream.repo}) (${t.upstream.license})`
    : "—";
  return `| \`${t.id}\` | ${t.display.label} | ${t.display.hint} | ${fns} | ${t.category ?? "—"} | ${t.pricing ?? "—"} | ${upstream} | @${t.maintainer ?? "—"} |`;
});

const doc = `# Tool catalog

<!-- GENERATED by scripts/gen-tools-catalog.mjs — do not edit by hand.
     Regenerate with: npm run gen:tools -->

${TOOL_MANIFESTS.length} tools registered through the manifest contract
([docs/PUBLIC_TOOL_ECOSYSTEM.md](docs/PUBLIC_TOOL_ECOSYSTEM.md)). Add yours:
[CONTRIBUTING_TOOLS.md](CONTRIBUTING_TOOLS.md).

Every webhook tool below is **model-callable in the chat loop** as
\`<id>__<fn>\` on direct chat turns (marketplace phase M2), in addition to its
\`POST /api/tools/<id>\` bridge endpoint. BYOK tools whose secrets aren't
configured on the server are stripped from the model's tool list per request.

| id | Label | What | Functions | Category | Pricing | Upstream | Maintainer |
|---|---|---|---|---|---|---|---|
${rows.join("\n")}
`;

writeFileSync(new URL("../TOOLS.md", import.meta.url), doc);
console.log(`TOOLS.md written (${TOOL_MANIFESTS.length} tools)`);
